def area(width, height):
    area = width * height
    print(f"The area of the rectangle is {area:.2f}!\n")

def perimeter(width, height):
    perimeter = (width * 2) + (height * 2)
    print(f"The perimeter of the rectangle is {perimeter:.2f}!\n")
